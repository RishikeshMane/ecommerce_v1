    public static getCountryId(countryName: string)
    {
      let countries = ECommerceDetails.getCountries();

      for (var country of countries) 
      {
        if (country.countryName === countryName)
          return country.countryLinkId;
      }
      
      return '';
    }
    
    public static getStateId(stateName: string)
    {
      let countries = ECommerceDetails.getCountries();

      for (var country of countries) 
      {
        for (var state of country.state)
        {
          if (state.stateName === stateName)
            return state.stateLinkId;
        }
      }
      
      return '';
    }
    
    public static getCityId(cityName: string)
    {
      let countries = ECommerceDetails.getCountries();

      for (var country of countries) 
      {
        for (var state of country.state)
        {
          for (var city of state.city)
          {
            if (city.cityName === cityName)
              return city.cityLinkId;
          }
        }
      }
      
      return '';      
    }
    
